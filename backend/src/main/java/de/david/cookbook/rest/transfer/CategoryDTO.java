package de.david.cookbook.rest.transfer;

public class CategoryDTO {

    private Long id;
    private String name;

    public CategoryDTO() {
    }

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
